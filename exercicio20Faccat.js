let valor1, valor2;

valor1 = parseInt(prompt("Digite o primeiro valor"))
valor2 = parseInt(prompt("Digite o segundo valor"))

if (valor1 > valor2) {
    alert("A ordem crescente dos números digitados é: " + valor2 + " , " + valor1)
}
else {
    alert("A ordem crescente dos números digitados é: " + valor1 + " , " + valor2)
}
